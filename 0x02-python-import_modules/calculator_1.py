#!/usr/bin/python3
def add(b, d):
    """My addition function

    Args:
        b: first integer
        d: second integer

    Returns:
        The return value. b + d
    """
    return (b + d)


def sub(b, d):
    """My subtraction function

    Args:
        b: first integer
        d: second integer

    Returns:
        The return value. b - d
    """
    return (b - d)


def mul(b, d):
    """My multiplication function

    Args:
        b: first integer
        d: second integer

    Returns:
        The return value. b * d
    """
    return (b * d)


def div(b, d):
    """My division function

    Args:
        b: first integer
        d: second integer

    Returns:
        The return value. b / d
    """
    return int(b / d)
